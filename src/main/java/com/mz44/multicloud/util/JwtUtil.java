package com.mz44.multicloud.util;

import io.jsonwebtoken.Jwts;
import io.jsonwebtoken.SignatureAlgorithm;
import io.jsonwebtoken.security.Keys;
import org.springframework.beans.factory.annotation.Value;
import org.springframework.stereotype.Component;

import java.security.Key;
import java.util.Date;

@Component
public class JwtUtil {
    private final Key key;
    private final long expirationMs;

    public JwtUtil(@Value("${multicloud.jwt.secret}") String secret,
                   @Value("${multicloud.jwt.expiration-ms}") long expirationMs) {
        // If secret length is appropriate, use as HMAC key; otherwise derive one
        byte[] keyBytes = secret.getBytes();
        this.key = Keys.hmacShaKeyFor(java.util.Arrays.copyOf(keyBytes, Math.max(32, keyBytes.length)));
        this.expirationMs = expirationMs;
    }

    public String generateToken(String username) {
        Date now = new Date();
        Date exp = new Date(now.getTime() + expirationMs);
        return Jwts.builder()
                .setSubject(username)
                .setIssuedAt(now)
                .setExpiration(exp)
                .signWith(key, SignatureAlgorithm.HS256)
                .compact();
    }

    public String extractUsername(String token) {
        return Jwts.parserBuilder().setSigningKey(key).build()
                .parseClaimsJws(token).getBody().getSubject();
    }
}
